package circulation;

public class dowhile_demo {
    public static void main(String[] args) {


        //与for和while的类型差不多，但do-while

        //第一次先执行再循环

        int i = 0;

        do{
            System.out.println("Hello World");
            i++;

        }while (i < 5);


        System.out.println(i);
    }
}
