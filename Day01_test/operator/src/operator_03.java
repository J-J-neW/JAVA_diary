public class operator_03 {
    public static void main(String[] args) {



     //类型转换，


        // 以小换大，列:char能转换成int类型
        int a = 10;
        char b = 20;

        int ch = (int)b;//20
        int ch2 = b;//20结果均为20，两种转换结果


        //转换表格：byte->shor->int->long->float->double
        //char类型也能转换成int类型

        System.out.println(ch);

        System.out.println(ch2);



        
    }
}
